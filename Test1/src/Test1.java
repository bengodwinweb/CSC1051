public class Test1 {

    public static void main(String[] args) {
        // 1. a string concatenated with one number and then concatenated
        // with another number (not concatenated)
        System.out.println("The number 2048 is " + 20 + 48);

        // 2. second, a string concatenated with the addition of two numbers
        System.out.println("The number 20 + 48 is " + (20 + 48));

        // 3. Third: one print statement in which the string itself is tabbed over
        // and has quote marks printed for one word. A second sentence which is on
        // a new line using escape characters.
        System.out.println("\tThis is a \"sentence\"\n" +
                "My second sentence is on a new line!");
    }
}
